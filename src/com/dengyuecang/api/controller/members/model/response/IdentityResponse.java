package com.dengyuecang.api.controller.members.model.response;

public class IdentityResponse{

	private String id;
	private String name;
	
	public IdentityResponse() {
		super();
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
	
	
}
